    private void lblDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblDeleteMouseClicked
        // TODO add your handling code here:
        String name = contact.getName();
        int result = JOptionPane.showConfirmDialog(null,"Sure? You want to delete?", "Warning",
               JOptionPane.YES_NO_OPTION,
               JOptionPane.QUESTION_MESSAGE);
        
        if(result == JOptionPane.YES_OPTION){
            System.out.println("DELETE " + contact.getContactId());
            ContactDB cdb = new ContactDB();
            try {
                cdb.deleteContact(contact);
                JOptionPane.showMessageDialog(null, ""+ name +" was deleted Successfully");
                new Home().setVisible(true);
                this.parent.setVisible(false);            
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }else if(result == JOptionPane.NO_OPTION){
            JOptionPane.showMessageDialog(null, ""+ name + " was not deleted");
        }
        
    }//GEN-LAST:event_lblDeleteMouseClicked

