    private void addData(Contact c) {
        lblName.setText(c.getName());
        DefaultListModel dlm = new DefaultListModel();
        c.getPhone().forEach((phone) -> {
            dlm.addElement(phone);
        });
        phones.setModel(dlm);
    }

